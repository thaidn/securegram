package org.telegram.ui.cells;

import android.content.Context;
import android.graphics.drawable.Drawable;
import android.text.TextUtils;
import android.util.TypedValue;
import android.view.Gravity;
import android.widget.FrameLayout;
import android.widget.ImageView;
import android.widget.TextView;

import org.telegram.android.AndroidUtilities;
import org.telegram.android.LocaleController;
import org.telegram.ui.components.LayoutHelper;

public class TextCell extends FrameLayout {

  private TextView textView;
  private TextView valueTextView;
  private ImageView imageView;
  private ImageView valueImageView;

  private boolean multiline;

  public TextCell(Context context) {
    super(context);

    textView = new TextView(context);
    textView.setTextColor(0xff212121);
    textView.setTextSize(TypedValue.COMPLEX_UNIT_DIP, 16);
    textView.setLines(1);
    textView.setMaxLines(1);
    textView.setSingleLine(true);
    textView.setEllipsize(TextUtils.TruncateAt.END);
    textView.setGravity(
        (LocaleController.isRTL ? Gravity.RIGHT : Gravity.LEFT) | Gravity.CENTER_VERTICAL);
    addView(
        textView,
        LayoutHelper.createFrame(
            LayoutHelper.MATCH_PARENT,
            LayoutHelper.MATCH_PARENT,
            (LocaleController.isRTL ? Gravity.RIGHT : Gravity.LEFT) | Gravity.TOP,
            LocaleController.isRTL ? 16 : 71,
            0,
            LocaleController.isRTL ? 71 : 16,
            0));

    valueTextView = new TextView(context);
    valueTextView.setTextColor(0xff2f8cc9);
    valueTextView.setTextSize(TypedValue.COMPLEX_UNIT_DIP, 16);
    valueTextView.setLines(1);
    valueTextView.setMaxLines(1);
    valueTextView.setSingleLine(true);
    valueTextView.setGravity(
        (LocaleController.isRTL ? Gravity.LEFT : Gravity.RIGHT) | Gravity.CENTER_VERTICAL);
    addView(
        valueTextView,
        LayoutHelper.createFrame(
            LayoutHelper.WRAP_CONTENT,
            LayoutHelper.MATCH_PARENT,
            (LocaleController.isRTL ? Gravity.LEFT : Gravity.RIGHT) | Gravity.TOP,
            LocaleController.isRTL ? 24 : 0,
            0,
            LocaleController.isRTL ? 0 : 24,
            0));

    imageView = new ImageView(context);
    imageView.setScaleType(ImageView.ScaleType.CENTER);
    addView(
        imageView,
        LayoutHelper.createFrame(
            LayoutHelper.WRAP_CONTENT,
            LayoutHelper.WRAP_CONTENT,
            (LocaleController.isRTL ? Gravity.RIGHT : Gravity.LEFT) | Gravity.CENTER_VERTICAL,
            LocaleController.isRTL ? 0 : 16,
            0,
            LocaleController.isRTL ? 16 : 0,
            0));

    valueImageView = new ImageView(context);
    valueImageView.setScaleType(ImageView.ScaleType.CENTER);
    addView(
        valueImageView,
        LayoutHelper.createFrame(
            LayoutHelper.WRAP_CONTENT,
            LayoutHelper.WRAP_CONTENT,
            (LocaleController.isRTL ? Gravity.LEFT : Gravity.RIGHT) | Gravity.CENTER_VERTICAL,
            LocaleController.isRTL ? 24 : 0,
            0,
            LocaleController.isRTL ? 0 : 24,
            0));
  }

  @Override
  protected void onMeasure(int widthMeasureSpec, int heightMeasureSpec) {
    super.onMeasure(
        widthMeasureSpec,
        multiline
            ? MeasureSpec.makeMeasureSpec(0, MeasureSpec.UNSPECIFIED)
            : MeasureSpec.makeMeasureSpec(AndroidUtilities.dp(48), MeasureSpec.EXACTLY));
  }

  public void setTextColor(int color) {
    textView.setTextColor(color);
  }

  public void setText(String text) {
    textView.setText(text);
    imageView.setVisibility(INVISIBLE);
    valueTextView.setVisibility(INVISIBLE);
    valueImageView.setVisibility(INVISIBLE);
  }

  public void setTextAndIcon(String text, int resId) {
    textView.setText(text);
    imageView.setImageResource(resId);
    imageView.setVisibility(VISIBLE);
    valueTextView.setVisibility(INVISIBLE);
    valueImageView.setVisibility(INVISIBLE);
  }

  public void setMultiline(boolean value) {
    if (multiline == value) {
      return;
    }
    multiline = value;
    if (value) {
      textView.setSingleLine(false);
      textView.setPadding(0, AndroidUtilities.dp(6), 0, AndroidUtilities.dp(6));
    } else {
      textView.setLines(1);
      textView.setMaxLines(1);
      textView.setSingleLine(true);
    }
    requestLayout();
  }

  public void setTextAndValue(String text, String value) {
    textView.setText(text);
    valueTextView.setText(value);
    valueTextView.setVisibility(VISIBLE);
    imageView.setVisibility(INVISIBLE);
    valueImageView.setVisibility(INVISIBLE);
  }

  public void setTextAndValueDrawable(String text, Drawable drawable) {
    textView.setText(text);
    valueImageView.setVisibility(VISIBLE);
    valueImageView.setImageDrawable(drawable);
    valueTextView.setVisibility(INVISIBLE);
    imageView.setVisibility(INVISIBLE);
  }
}
